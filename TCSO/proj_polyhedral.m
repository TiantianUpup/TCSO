function X=proj_polyhedral(Y)
    [n1,n2]=size(Y);
    X=zeros(n1,n2);
    n1=n1/3;
    n2=n2/3;
    Y2=Y(1:n1,n2+1:2*n2);
    Y3=Y(1:n1,2*n2+1:3*n2);
    Y4=Y(n1+1:2*n1,1:n2);
    Y6=Y(n1+1:2*n1,2*n2+1:3*n2);
    Y7=Y(2*n1+1:3*n1,1:n2);
    Y8=Y(2*n1+1:3*n1,n2+1:2*n2);

    X(1:n1,n2+1:2*n2)=(Y2-Y4)/2;
    X(1:n1,2*n2+1:3*n2)=(Y3-Y7)/2;
    X(n1+1:2*n1,1:n2)=(Y4-Y2)/2;
    X(n1+1:2*n1,2*n2+1:3*n2)=(Y6-Y8)/2;
    X(2*n1+1:3*n1,1:n2)=(Y7-Y3)/2;
    X(2*n1+1:3*n1,n2+1:2*n2)=(Y8-Y6)/2;
end    